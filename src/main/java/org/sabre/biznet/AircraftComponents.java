package org.sabre.biznet;

public class AircraftComponents {

    private String serialNo;
    private String flightNo;
    private String flightMode;
    private String componentName;
    private String componentModel;
    private String componentManufacturer;
    private String componentManufacturingDate;
    private String componentExpiryDate;
    private String airline;

    public String getSerialNo() {
        return serialNo;
    }

    public void setSerialNo(String serialNo) {
        this.serialNo = serialNo;
    }

    public String getFlightNo() {
        return flightNo;
    }

    public void setFlightNo(String flightNo) {
        this.flightNo = flightNo;
    }

    public String getFlightMode() {
        return flightMode;
    }

    public void setFlightMode(String flightMode) {
        this.flightMode = flightMode;
    }

    public String getComponentName() {
        return componentName;
    }

    public void setComponentName(String componentName) {
        this.componentName = componentName;
    }

    public String getComponentModel() {
        return componentModel;
    }

    public void setComponentModel(String componentModel) {
        this.componentModel = componentModel;
    }

    public String getComponentManufacturer() {
        return componentManufacturer;
    }

    public void setComponentManufacturer(String componentManufacturer) {
        this.componentManufacturer = componentManufacturer;
    }

    public String getComponentManufacturingDate() {
        return componentManufacturingDate;
    }

    public void setComponentManufacturingDate(String componentManufacturingDate) {
        this.componentManufacturingDate = componentManufacturingDate;
    }

    public String getComponentExpiryDate() {
        return componentExpiryDate;
    }

    public void setComponentExpiryDate(String componentExpiryDate) {
        this.componentExpiryDate = componentExpiryDate;
    }



    public String getAirline() {
        return airline;
    }

    public void setAirline(String airline) {
        this.airline = airline;
    }

}
